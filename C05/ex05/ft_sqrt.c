int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb <= 0)
		return (0);
	if (nb == 2)
		return (0);
	while (i * i < nb && i <= 46340)
		i++;
	if ((nb % i) == 0)
		return (i);
	else
		return (0);
}
